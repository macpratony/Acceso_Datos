package A7_Json;

public class Notas {
	
	String materia;
	int nota;
	
	public Notas(String materia, int nota) {
		super();
		this.materia = materia;
		this.nota = nota;
	}
	public String getMateria() {
		return materia;
	}
	public void setMateria(String materia) {
		this.materia = materia;
	}
	public int getNota() {
		return nota;
	}
	public void setNota(int nota) {
		this.nota = nota;
	}
	
	
	
	

}
